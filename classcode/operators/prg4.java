class unary{
	public static void main(String[] args){
		int x=4,y=7;

		System.out.println(++x);  //5
		System.out.println(++y);  //8

		System.out.println(x--);  //5
		System.out.println(--x);  //3
		System.out.println(y++);  //8
		System.out.println(--y);  //8

		System.out.println(x);   //3  
		System.out.println(y);   //8
	}
}
